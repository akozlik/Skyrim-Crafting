- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier] autorelease];
    }
    
    Ingredient *ingredient = [self.ingredients objectAtIndex:indexPath.row];
    NSString *detailLabel;
    
    if ([ingredient.primary.name isEqualToString:self.title])
    {
        detailLabel = @"primary";
    } else if ([ingredient.secondary.name isEqualToString:self.title])
    {
        detailLabel = @"secondary";
    }
    else if ([ingredient.tertiary.name isEqualToString:self.title])
    {
        detailLabel = @"tertiary";        
    }
    else if ([ingredient.quaternary.name isEqualToString:self.title])
    {
        detailLabel = @"quaternary";
    } else
    {
        detailLabel = @"";
    }
    
    cell.detailTextLabel.text = detailLabel;
    cell.textLabel.text = ingredient.name;
    cell.selectionStyle = UITableViewCellSelectionStyleNone;
    
    // Configure the cell...
    
    return cell;
}

